$(document).ready(function () {
    // Maze size/dimensions
    var mazeHeight = 400;
    var mazeWidth = 400;

    // Player starting position
    var playerX = 20;
    var playerY = 20;

    var timer = 60; // 60 seconds

    // Update timer display
    function updateTimer() {
        $('#timer').text(timer);
    }

    // Countdown function
    function countdown() {
        if (timer > 0) {
            timer--;
            updateTimer();
            setTimeout(countdown, 1000); // Call countdown again after 1 second
        } else {
            // Game over
            alert("Time's up! Game over.");
        }
    }

    // Start countdown
    countdown();

    // Move function for player
    function movePlayer(dx, dy) {
        var newX = playerX + dx;
        var newY = playerY + dy;
        // Verify new position if inside the maze walls
        if (newX >= 0 && newX < mazeWidth && newY >= 0 && newY < mazeHeight) {
            // Check to see if the new position is a wall or not
            if (!$('#maze').find('.wall').is('[style="top: ' + newY + 'px; left: ' + newX + 'px;"]')) {
                // We can move the player
                playerX = newX;
                playerY = newY;
                $('#player').css({ top: playerY + 'px', left: playerX + 'px' });
                // Check to see if the player reaches the end
                if (playerX === 380 && playerY === 380) {
                    alert("Congratulations! You've reached the end of the maze!");
                }
            }
        }
    }
    
    $(document).keydown(function (e) {
        switch (e.which) {
            case 37:
                movePlayer(-20, 0);
                break;
            case 38:
                movePlayer(0, -20);
                break;
            case 39:
                movePlayer(20, 0);
                break;
            case 40:
                movePlayer(0, 20);
                break;     
        }
    });

    // Create maze walls
    var wallCoordinates = [
        // Add maze walls here
        { top: 20, left: 0 },    // wall1
        { top: 20, left: 20 },   // wall2
        { top: 0, left: 40 },    // wall3
        // Add more walls as needed
    ];

    // Add additional walls to create a maze-like structure
    for (var i = 0; i < 100; i++) {
        var randomTop = Math.floor(Math.random() * (mazeHeight / 20)) * 20;
        var randomLeft = Math.floor(Math.random() * (mazeWidth / 20)) * 20;
        wallCoordinates.push({ top: randomTop, left: randomLeft });
    }

    // Append walls to the maze
    for (var i = 0; i < wallCoordinates.length; i++) {
        $('#maze').append('<div class="wall" style="top: ' + wallCoordinates[i].top + 'px; left: ' + wallCoordinates[i].left + 'px;"></div>');
    }

    // Create player
    $('#maze').append('<div class="player" id="player" style="top: ' + playerY + 'px; left: ' + playerX + 'px;"></div>');
});


